    // MOSTRAR INCIDENCIAS EN EL JTABLE
    public void verIncidencias() {

        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"Nombre", "Descripción", "Descripción Técnico", "Horas", "Estado", "Fecha", "Fecha Inicial Reparación", "Fecha Final Reparación", "Urgencia", "Ubicación", "Observaciones"});

        String[] a = new String[11];

        conectar = new Conectar();
        Connection conexion = conectar.getConexion();

        try {

            PreparedStatement ps = conexion.prepareStatement("select p.nombre_completo, m.descripcion, m.desc_tecnica, m.horas, e.estado, m.fecha, m.fecha_ini_rep, m.fecha_fin_rep, ur.urgencia, u.ubicacion, m.observaciones\n"
                    + "from man_incidencias m inner join fp_profesor p \n"
                    + "on p.id_profesor = m.id_profesor_crea\n"
                    + "inner join man_ubicacion u \n"
                    + "on u.id_ubicacion = m.id_ubicacion\n"
                    + "inner join man_estado e\n"
                    + "on e.id_estado = m.id_estado\n"
                    + "inner join man_urgencia ur\n"
                    + "on ur.id_urgencia = m.nivel_urgencia;");

            ResultSet rs = ps.executeQuery();

            String nombre_completo = null;
            String descripcion = null;
            String desc_tecnica = null;
            String horas = null;
            String estado = null;
            String fecha = null;
            String fecha_ini = null;
            String fecha_fin = null;
            String urgencia = null;
            String ubicacion = null;
            String observaciones = null;

            while (rs.next()) {

                a[0] = rs.getString(1);
                a[1] = rs.getString(2);
                a[2] = rs.getString(3);
                a[3] = rs.getString(4);
                a[4] = rs.getString(5);
                a[5] = rs.getString(6);
                a[6] = rs.getString(7);
                a[7] = rs.getString(8);
                a[8] = rs.getString(9);
                a[9] = rs.getString(10);
                a[10] = rs.getString(11);

                dtm.addRow(a);
            }

            jt_root.setModel(dtm);

        } catch (SQLException ex) {
            Logger.getLogger(screenProfesor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

